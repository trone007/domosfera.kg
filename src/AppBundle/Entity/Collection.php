<?php

namespace AppBundle\Entity;

/**
 * Сущность Doctrine для
 * работы с таблицей collection БД.
 *
 *
 * Хранение данных наименований коллекций
 */
class Collection
{
    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $catalog;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set name
     *
     * @param string $name
     *
     * @return Collection
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set catalog
     *
     * @param string $catalog
     *
     * @return Collection
     */
    public function setCatalog($catalog)
    {
        $this->catalog = $catalog;

        return $this;
    }

    /**
     * Get catalog
     *
     * @return string
     */
    public function getCatalog()
    {
        return $this->catalog;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
    /**
     * @var string
     */
    private $property;


    /**
     * Set property
     *
     * @param string $property
     *
     * @return Collection
     */
    public function setProperty($property)
    {
        $this->property = $property;

        return $this;
    }

    /**
     * Get property
     *
     * @return string
     */
    public function getProperty()
    {
        return $this->property;
    }
}
