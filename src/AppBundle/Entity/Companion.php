<?php

namespace AppBundle\Entity;

/**
 * Сущность Doctrine для
 * работы с таблицей companion БД.
 *
 *
 * Хранение данных корневых артикулов
 */
class Companion
{
    /**
     * @var string
     */
    private $complect;

    /**
     * @var string
     */
    private $vendorCode;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set complect
     *
     * @param string $complect
     *
     * @return Companion
     */
    public function setComplect($complect)
    {
        $this->complect = $complect;

        return $this;
    }

    /**
     * Get complect
     *
     * @return string
     */
    public function getComplect()
    {
        return $this->complect;
    }

    /**
     * Set vendorCode
     *
     * @param string $vendorCode
     *
     * @return Companion
     */
    public function setVendorCode($vendorCode)
    {
        $this->vendorCode = $vendorCode;

        return $this;
    }

    /**
     * Get vendorCode
     *
     * @return string
     */
    public function getVendorCode()
    {
        return $this->vendorCode;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
    /**
     * @var string
     */
    private $name;


    /**
     * Set name
     *
     * @param string $name
     *
     * @return Companion
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
