<?php

namespace AppBundle\Entity;

/**
 * Сущность Doctrine  для работы с таблицей region БД.
 *
 * Хранение данных организаций из раздела настроек Api домосферы регионы
 */
class Region
{
    /**
     * @var string
     */
    private $parentUuid;

    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $urlName;

    /**
     * @var string
     */
    private $uuid;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set parentUuid
     *
     * @param string $parentUuid
     *
     * @return Region
     */
    public function setParentUuid($parentUuid)
    {
        $this->parentUuid = $parentUuid;

        return $this;
    }

    /**
     * Get parentUuid
     *
     * @return string
     */
    public function getParentUuid()
    {
        return $this->parentUuid;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Region
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set urlName
     *
     * @param string $urlName
     *
     * @return Region
     */
    public function setUrlName($urlName)
    {
        $this->urlName = $urlName;

        return $this;
    }

    /**
     * Get urlName
     *
     * @return string
     */
    public function getUrlName()
    {
        return $this->urlName;
    }

    /**
     * Set uuid
     *
     * @param string $uuid
     *
     * @return Region
     */
    public function setUuid($uuid)
    {
        $this->uuid = $uuid;

        return $this;
    }

    /**
     * Get uuid
     *
     * @return string
     */
    public function getUuid()
    {
        return $this->uuid;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
}
