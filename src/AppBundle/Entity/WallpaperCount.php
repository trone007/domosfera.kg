<?php

namespace AppBundle\Entity;

/**
 * Сущность Doctrine  для работы с таблицей wallpaper_count БД.
 *
 * Хранение информации о остатках товаров в магазинах. Поставляется из 1C. При помощи API.
 *
 * @todo переименовать в товары
 */
class WallpaperCount
{
    /**
     * @var string
     */
    private $shop;

    /**
     * @var string
     */
    private $wallpaperUuid;

    /**
     * @var string
     */
    private $count;

    /**
     * @var string
     */
    private $showcaseCount;

    /**
     * @var string
     */
    private $totalCount;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set shop
     *
     * @param string $shop
     *
     * @return WallpaperCount
     */
    public function setShop($shop)
    {
        $this->shop = $shop;

        return $this;
    }

    /**
     * Get shop
     *
     * @return string
     */
    public function getShop()
    {
        return $this->shop;
    }

    /**
     * Set wallpaperUuid
     *
     * @param string $wallpaperUuid
     *
     * @return WallpaperCount
     */
    public function setWallpaperUuid($wallpaperUuid)
    {
        $this->wallpaperUuid = $wallpaperUuid;

        return $this;
    }

    /**
     * Get wallpaperUuid
     *
     * @return string
     */
    public function getWallpaperUuid()
    {
        return $this->wallpaperUuid;
    }

    /**
     * Set count
     *
     * @param string $count
     *
     * @return WallpaperCount
     */
    public function setCount($count)
    {
        $this->count = $count;

        return $this;
    }

    /**
     * Get count
     *
     * @return string
     */
    public function getCount()
    {
        return $this->count;
    }

    /**
     * Set showcaseCount
     *
     * @param string $showcaseCount
     *
     * @return WallpaperCount
     */
    public function setShowcaseCount($showcaseCount)
    {
        $this->showcaseCount = $showcaseCount;

        return $this;
    }

    /**
     * Get showcaseCount
     *
     * @return string
     */
    public function getShowcaseCount()
    {
        return $this->showcaseCount;
    }

    /**
     * Set totalCount
     *
     * @param string $totalCount
     *
     * @return WallpaperCount
     */
    public function setTotalCount($totalCount)
    {
        $this->totalCount = $totalCount;

        return $this;
    }

    /**
     * Get totalCount
     *
     * @return string
     */
    public function getTotalCount()
    {
        return $this->totalCount;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
}
